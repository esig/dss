package eu.europa.esig.dss;

public class CertificatePolicy {

	private String oid;
	private String cpsUrl;

	public String getOid() {
		return oid;
	}

	public void setOid(String oid) {
		this.oid = oid;
	}

	public String getCpsUrl() {
		return cpsUrl;
	}

	public void setCpsUrl(String cpsUrl) {
		this.cpsUrl = cpsUrl;
	}

}
